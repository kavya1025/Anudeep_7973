package Lab_5;

public class NarrowCasting {
	    public static void main(String[] args) 
	    {
	        double Raj = 143.143; 
	        int Ratan = (int) Raj;

	        System.out.println("Original double value: " + Raj);
	        System.out.println("Narrowed int value: " + Ratan);
	    }
}